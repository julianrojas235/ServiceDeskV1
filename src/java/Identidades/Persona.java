package Identidades;

/**
 *
 * @author Usuario1
 */
public class Persona {
    private int id;
    private String name;
    private String ape;
    private String mail;
    private String pass;

    

    public Persona() {
        this.id = 0;
        this.name = null;
        this.mail = null;
        this.pass = null;
        this.ape = null;
    }

    public String getApe() {
        return ape;
    }

    public void setApe(String ape) {
        this.ape = ape;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
    
}
